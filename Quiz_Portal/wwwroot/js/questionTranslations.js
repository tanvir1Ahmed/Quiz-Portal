// Function to get translated question
function getTranslatedQuestion(question) {
    const lang = getCurrentLanguage();
    const translation = questionTranslations[question.id];
    
    if (translation && translation[lang]) {
        return {
            ...question,
            questionText: translation[lang].questionText,
            optionA: translation[lang].optionA,
            optionB: translation[lang].optionB,
            optionC: translation[lang].optionC
        };
    }
    
    // Fallback to original question if no translation found
    return question;
}
